public class EX12 {
    public static void main(String[] args)
    {
        int colums = 10;
        int rows = 5;
        for(int i = 1; i <= rows; i++)
        {
            for(int j = 1; j <= colums; j++)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
